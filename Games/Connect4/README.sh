# Connect Four 🔴🟡

A classic two-player Connect Four game built for the browser.

## Overview

Connect Four is a strategy board game where two players take turns dropping colored discs into a vertical grid. The first player to connect **four discs in a row** — horizontally, vertically, or diagonally — wins the round!

## Players

| Player | Color |
|--------|-------|
| Player 1 | 🔴 Red |
| Player 2 | 🟡 Yellow |

## How to Play

1. Players alternate turns, starting with **Player 1 (Red)**.
2. On your turn, click a column to drop your disc into it.
3. The disc falls to the lowest available row in that column.
4. The first player to get **four discs in a row** wins!
   - Horizontally ➡️
   - Vertically ⬆️
   - Diagonally ↗️ or ↘️
5. If the board fills up with no winner, the game is a **draw**.

## Controls & Buttons

| Button | Description |
|--------|-------------|
| **Next Round** | Start a new round while keeping the current scores |
| **New Game** | Reset the board and start fresh (scores are kept) |
| **Reset Scores** | Clear both players' scores and start over completely |

## Getting Started

Simply open the game in your browser and start playing — no installation required!

