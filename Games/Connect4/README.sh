# Connect4 (Browser)

This is a 2-player Connect4 game in the browser.

## How to run

1. Open this folder.
2. Double-click `connect_4.html`.
3. It opens in your web browser.

## How to play

1. Player 1 and Player 2 take turns.
2. Click a column to drop your piece.
3. First player to connect 4 pieces wins.

You can win by connecting 4:

- horizontally
- vertically
- diagonally

## Buttons

- `Next Round` - new board, keep scores
- `New Game` - new board
- `Reset Scores` - set both scores to zero